package com.sda;

public class DetyreAfishim {
    public static void main(String[] args) {
        int numriInteger = 10;
        double numriDecimal = 3.14;
        char karakteri = 'A';
        boolean logjika = true;
        String teksti = "Përshëndetje";
        System.out.println("Vlera e numrit të plotë: " + numriInteger);
        System.out.println("Vlera e numrit decimal: " + numriDecimal);
        System.out.println("Vlera e karakterit: " + karakteri);
        System.out.println("Vlera logjike: " + logjika);
        System.out.println("Vlera e tekstit: " + teksti);
    }
}
